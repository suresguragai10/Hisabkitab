const BS_START_YEAR = 2000;
// AD date of BS 2000 Baishakh 1 = April 13, 1943
const AD_EPOCH = new Date(1943, 3, 14); // April 14, 1943 (month is 0-indexed)

export const BS_MONTHS_NP = ["बैशाख","जेठ","असार","साउन","भाद्र","असोज","कार्तिक","मंसिर","पुष","माघ","फागुन","चैत्र"];
export const BS_MONTHS_EN = ["Baishakh","Jestha","Ashadh","Shrawan","Bhadra","Ashwin","Kartik","Mangsir","Poush","Magh","Falgun","Chaitra"];
export const DAYS_NP = ["आइत","सोम","मंगल","बुध","बिही","शुक्र","शनि"];

// Convert AD date to BS
export function adToBs(adDate) {
  const ad = new Date(adDate);
  ad.setHours(0,0,0,0);
  // Days since epoch
  const epoch = new Date(AD_EPOCH);
  epoch.setHours(0,0,0,0);
  let daysDiff = Math.round((ad - epoch) / 86400000);

  let bsYear = BS_START_YEAR;
  let bsMonth = 0;
  let bsDay = 1;

  for (let y = 0; y < BS_MONTHS.length; y++) {
    for (let m = 0; m < 12; m++) {
      if (daysDiff < BS_MONTHS[y][m]) {
        bsYear = BS_START_YEAR + y;
        bsMonth = m;
        bsDay = daysDiff + 1;
        return { year: bsYear, month: bsMonth, day: bsDay };
      }
      daysDiff -= BS_MONTHS[y][m];
    }
  }
  // Fallback for dates beyond table
  return { year: BS_START_YEAR + BS_MONTHS.length - 1, month: 11, day: 30 };
}

// Convert BS date to AD
export function bsToAd(bsYear, bsMonth, bsDay) {
  const yearIdx = bsYear - BS_START_YEAR;
  if (yearIdx < 0 || yearIdx >= BS_MONTHS.length) return null;

  let days = 0;
  for (let y = 0; y < yearIdx; y++) {
    for (let m = 0; m < 12; m++) days += BS_MONTHS[y][m];
  }
  for (let m = 0; m < bsMonth; m++) days += BS_MONTHS[yearIdx][m];
  days += bsDay - 1;

  const result = new Date(AD_EPOCH);
  result.setDate(result.getDate() + days);
  return result;
}

// Format BS date as string
export function formatBs(adDate, lang = "en") {
  const bs = adToBs(adDate);
  const monthName = lang === "np" ? BS_MONTHS_NP[bs.month] : BS_MONTHS_EN[bs.month];
  const day = lang === "np" ? toNepaliDigits(bs.day) : bs.day;
  const year = lang === "np" ? toNepaliDigits(bs.year) : bs.year;
  return `${year} ${monthName} ${day}`;
}

// Format dual date: BS + AD
export function formatDualDate(adDate, lang = "en") {
  const bs = formatBs(adDate, lang);
  const ad = new Date(adDate).toLocaleDateString("en-GB", { day: "2-digit", month: "short", year: "numeric" });
  return `${bs} (${ad})`;
}

// Get current BS fiscal year label e.g. "2081-82"
export function bsFiscalYearFor(adDate = new Date()) {
  const bs = adToBs(adDate);
  // FY starts Shrawan (month index 3)
  const fyStartYear = bs.month >= 3 ? bs.year : bs.year - 1;
  const endShort = String((fyStartYear + 1) % 100).padStart(2, "0");
  return `${fyStartYear}-${endShort}`;
}

export function currentBsFiscalYear() {
  return bsFiscalYearFor(new Date());
}

// Convert digit to Nepali
export function toNepaliDigits(n) {
  const np = ["०","१","२","३","४","५","६","७","८","९"];
  return String(n).replace(/\d/g, d => np[d]);
}

// Today in BS
export function todayBs() {
  return adToBs(new Date());
}
